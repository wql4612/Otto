# voice_command.py
# -*- coding: utf-8 -*-
"""
语音指令关键词匹配 — 从自然语言中识别设备控制指令

在用户与小猫对话时，检测语句中是否包含预设的设备控制关键词。
若匹配则返回对应的指令 ID，ESP32 收到后执行设备控制动作。
不匹配则返回 None，走 AI 对话模式。
"""

from typing import Optional, Tuple

# ====================================================================
# 关键词 → ESP32 VoiceCommand 映射
# 格式: (关键词列表, ESP32 指令 ID)
# 按优先级排列：长关键词放前面，避免短关键词误匹配
# ====================================================================
VOICE_COMMAND_MAP = [
    # ---- 灯光控制 ----
    (["开灯", "打开灯", "开一下灯", "把灯打开", "灯光打开", "灯打开"],     "CMD_LIGHT_ON"),
    (["关灯", "关闭灯", "关一下灯", "把灯关了", "灯光关闭", "灯关了"],     "CMD_LIGHT_OFF"),
    (["亮一点", "亮一些", "亮点", "调亮", "调亮一些", "亮一点吧"],        "CMD_BRIGHTER"),
    (["暗一点", "暗一些", "暗点", "调暗", "调暗一些", "暗一点吧"],        "CMD_DIMMER"),

    # ---- 风扇控制 ----
    (["开风扇", "打开风扇", "风扇打开", "把风扇打开", "启动风扇"],        "CMD_FAN_ON"),
    (["关风扇", "关闭风扇", "风扇关了", "把风扇关了", "停风扇"],          "CMD_FAN_OFF"),

    # ---- 音响 ----
    (["放音乐", "播放音乐", "放歌", "播音乐", "来点音乐", "放首音乐"],    "CMD_MUSIC_ON"),
    (["停下", "停止", "别放了", "不要放了", "住手", "别动了", "停"],      "CMD_STOP"),

    # ---- 互动 ----
    (["你好", "嗨", "哈喽", "hello", "hi"],                               "CMD_HELLO"),
    (["过来", "过来一下", "来一下", "过来呀", "到这里来"],                "CMD_COME_HERE"),

    # ---- 模式 ----
    (["休闲模式", "放松模式", "休息模式"],                                "CMD_RELAX_MODE"),
    (["工作模式", "专注模式", "学习模式"],                                "CMD_WORK_MODE"),
    (["回家模式", "到家了", "回来了"],                                    "CMD_HOME_MODE"),
    (["离开模式", "离家模式", "出门了", "走了"],                          "CMD_AWAY_MODE"),

    # ---- 查询 ----
    (["现在几点", "几点了", "什么时间", "几点钟了"],                      "CMD_WHAT_TIME"),
]


def match_command(text: str) -> Optional[Tuple[str, str]]:
    """
    从用户语音文本中匹配设备控制指令。

    参数:
        text: 用户语音识别文本（ASR final 结果）

    返回:
        (command_id, matched_keyword) 如果匹配成功
        None 如果没有匹配 —— 走 AI 对话模式
    """
    text = text.strip()
    if not text:
        return None

    for keywords, cmd_id in VOICE_COMMAND_MAP:
        for kw in keywords:
            if kw in text:
                return (cmd_id, kw)

    return None
